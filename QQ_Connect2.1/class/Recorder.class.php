require_once(QQ_CLASS_PATH."ErrorCase.class.php");
class Recorder{
    private static $data;
    private $inc;
    private $error;

    public function __construct(){
        $this->error = new ErrorCase();
        //此处配置appid、appkey、回调域名url。(https://connect.qq.com/)
        $this->inc ->appid='101533813';
        $this->inc ->appkey='dfa675774353b906f4ed637b2e3d68f0';
        $this->inc ->callback='http://pay.neweb.top/callback.php';
        $this->inc ->scope='get_user_info';//需要获取的权限
        $this->inc ->errorReport='true';$this->inc ->storageType='file';
        
        if(empty($this->inc)){
            $this->error->showError("20001");
        }

        if(empty($_SESSION['QC_userData'])){
            self::$data = array();
        }else{
            self::$data = $_SESSION['QC_userData'];
        }
    }

    public function write($name,$value){
        self::$data[$name] = $value;
    }

    public function read($name){
        if(empty(self::$data[$name])){
            return null;
        }else{
            return self::$data[$name];
        }
    }

    public function readInc($name){
        if(empty($this->inc->$name)){
            return null;
        }else{
            return $this->inc->$name;
        }
    }

    public function delete($name){
        unset(self::$data[$name]);
    }

    function __destruct(){
        $_SESSION['QC_userData'] = self::$data;
    }
}
